package org.jtransfo.object;

public class SimpleWithEnumDomain extends SimpleBaseDomain {

    private String b;
    private String c;
    private int i;
    private SomeEnum some;

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public SomeEnum getSome() {
        return some;
    }

    public void setSome(SomeEnum some) {
        this.some = some;
    }
}
